// Helper functions for random selection
export const getRandomElements = (array, count) => {
  const shuffled = [...array].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getRandomEncounters = () => getRandomElements(encounters, 3);
export const getRandomItems = () => getRandomElements(items, 3);
export const getRandomMonsters = () => getRandomElements(monsters, 3);
export const getRandomEvents = () => getRandomElements(events, 3);

// Helper function to get random encounters by type
export const getEncountersByType = (type, count) => {
  const typeEncounters = encounters.filter(e => e.type === type);
  return getRandomElements(typeEncounters, count);
};

// Helper function to get random encounters by type and age
export const getRandomEncounterOfType = (type, currentAge) => {
  const typeEncounters = encounters.filter(encounter => {
    // If encounter has an age property, check if it matches current age
    if (encounter.type === type) {
      if (encounter.age) {
        return encounter.age === currentAge;
      }
      // If no age property, it's available in all ages
      return true;
    }
    return false;
  });

  if (typeEncounters.length === 0) {
    console.warn(`No encounters found for type ${type} in age ${currentAge}`);
    return null;
  }

  return typeEncounters[Math.floor(Math.random() * typeEncounters.length)];
};
